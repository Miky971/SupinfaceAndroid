package com.labojava.supinface.android.model;


public class User {
	
	private int id;
	private String idBooster;
	private String firstName;
	private String lastName;
	private String email;
	private String promo;
	private String pictureUrl;
	private String info_Sup;
	private Campus campus;
	private Lab lab;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getIdBooster() {
		return idBooster;
	}
	public void setIdBooster(String idBooster) {
		this.idBooster = idBooster;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPromo() {
		return promo;
	}
	public void setPromo(String promo) {
		this.promo = promo;
	}
	public String getPictureUrl() {
		return pictureUrl;
	}
	public void setPictureUrl(String pictureUrl) {
		this.pictureUrl = pictureUrl;
	}
	public String getInfo_Sup() {
		return info_Sup;
	}
	public void setInfo_Sup(String info_Sup) {
		this.info_Sup = info_Sup;
	}
	public Campus getCampus() {
		return campus;
	}
	public void setCampus(Campus campus) {
		this.campus = campus;
	}
	public Lab getLab() {
		return lab;
	}
	public void setLab(Lab lab) {
		this.lab = lab;
	}

	
	
}
